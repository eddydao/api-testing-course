let result = 'win';

// If else
if (result === 'win') {
    console.log('Chuc mung! Doi cua ban da chien thang');
} else if (result === 'lose') {
    console.log('Thua roi, nhung dung bo cuoc');
} else if (result === 'draw') {
    console.log('Tran dau hoa, that dang tiec');
}


// swtich case
switch (result) {
    case 'win':
        console.log('Chuc mung! Doi cua ban da chien thang');
        break;
    case 'lose':
        console.log('Thua roi, nhung dung bo cuoc');
        break;
    case 'draw':
        console.log('Tran dau hoa, that dang tiec');
        break;
}
