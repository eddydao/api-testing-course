
let themeMode = 'dark';

if (themeMode === 'dark') {
    console.log('Che do toi da duoc bat');
} else if (themeMode === 'light') {
    console.log('Che do sang da duoc bat');
}

switch (themeMode) {
    case 'dark':
        console.log('Che do toi da duoc bat');
        break;
    case 'light':
        console.log('Che do sang da duoc bat');
        break;
}

